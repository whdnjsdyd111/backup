package 챕터11_기본API클래스.ㄷ_Object클래스.ㄹ_객체복제_clone;

public class 객체복제 {
	// 객체 복제는 원본 객체의 필드값과 동일한 값을 가지는 새로운 객체를 생성하는 것을 말한다.
	// 객체를 복제하는 이유는 원본 객체를 안전하게 보호하기 위해서이다.
	// 신뢰하지 않는 영역으로 원본 객체를 넘겨 작업할 경우 원본 객체의 데이터가 훼손될 수 있기 때문에 복제된 객체를 만들어 신뢰하지 않는 영역으로 넘기는 것이 좋다.
	// 복제된 객체의 데이터가 훼손되더라도 원본 객체는 아무런 영향을 받지 않기 때문에 안전하게 데이터를 보호할 수 있게 된다.
	// 복제에는 얕은 복제와 깊은 복제가 있다.

}
