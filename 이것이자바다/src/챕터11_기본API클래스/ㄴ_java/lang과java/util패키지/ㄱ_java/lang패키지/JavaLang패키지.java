package 챕터11_기본API클래스.ㄴ_java.lang과java.util패키지.ㄱ_java.lang패키지;

public class JavaLang패키지 {
	// java.lang 패키지는 자바 프로그램의 기본적인 클래스를 담고 있는 패키지이다.
	// 그렇기 때문에 java.lang 패키지에 있는 클래스와 인터페이스는 import 없이 사용할 수 있다.
	// 지금까지 이용한 String과 System 클래스도 java.lang 패키지에 포함되어 있기 때문에 import하지 않고 사용했다.
	// 다음은 이번 장에 소개할 java.lang 패키지에 속하는 주요 클래스와 간략한 용도에 대해 설명한 표이다.
	
	//	클래스								용도
	//	Object								- 자바 클래스의 최상위 클래스로 사용
	//
	//										- 표준 입력 장치(키보드)로부터 데이터를 입력받을 때 사용
	//	System								- 표준 출력 장치(모니터)로 출력하기 위해 사용
	//										- 자바 가상 기계를 종료시킬 때 사용
	//										- 쓰레기 수집기를 실행 요청할 때 사용
	//
	//	Class								- 클래스를 메모리로 로딩할 때 사용
	//
	//	String								- 문자열을 저장하고 여러 가지 정보를 얻을 때 사용
	//
	//	StringBuffer, StringBuilder			- 문자열을 저장하고 내부 문자열을 조작할 때 사용
	//
	//	Math								- 수학 함수를 이용할 때 사용
	
	//				Byte, Short, Character	- 기본 타입의 데이터를 갖는 객체를 만들 때 사용
	//	Wrapper		Integer, Float, Double	- 문자열을 기본 타입으로 변환할 때 사용
	//				Boolean, Long			- 입력값 검사에 사용
}
