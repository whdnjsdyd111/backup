package 챕터11_기본API클래스.ㄴ_java.lang과java.util패키지.ㄴ_java.util패키지;

public class JavaUtil패키지 {
	// java.util 패키지는 자바 프로그램 개발에 조미료 같은 역할을 하는 클래스를 답고 있다
	// java.util 패키지는 컬렉션 클래스들이 대부분을 차지하고 있다.
	// 컬렉션 클래스들은 15장에서 자세히 학습하고 이번 장에서는 여섯 가지 클래스만 소개하려고 한다.
	
	//	클래스				용도
	//	Arrays				- 배열을 조작(비교, 복사, 정렬, 찾기)할 때 사용
	//	Calendar			- 운영체제의 날짜와 시간을 얻을 때 사용
	//	Date				- 날짜와 시간 정보를 저장하는 클래스
	//	Object				- 객체 비교, 널(null) 여부 등을 조사할 때 사용
	//	StringTokenizer		- 특정 문자로 구분된 문자열을 뽑아낼 때 사용
	//	Random				- 난수를 얻을 때 사용
	
}
