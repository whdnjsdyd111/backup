package 챕터11_기본API클래스.ㅁ_System클래스;

public class System클래스 {
	// 자바 프로그램은 운영체제상에서 바로 실행되는 것이 아니라 JVM 위에서 실행된다.
	// 따라서 운영체제의 모든 기능을 자바 코드로 직접 접근하기란 어렵다.
	// 하지만 java.lang 패키지에 속하는 System 클래스를 이용하면 운영체제의 일부 기능을 이용할 수 있다.
	// 즉, 프로그램 종료, 키보드로부터 입력, 모니터로 출력, 메모리 정리, 현재 시간 읽기, 시스템 프로퍼티 읽기, 환경 변수 일기 등이 가능하다
	// System 클래스의 모든 필드와 메소드는 정적(static) 필드와 정적(static) 메소드로 구성되어있다.
}
